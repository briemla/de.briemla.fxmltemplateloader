package de.briemla.fxmltemplateloader;

public class BasicTypeValue implements IValue {

	private final Object value;

	public BasicTypeValue(Object object) {
		super();
		this.value = object;
	}

	@Override
	public Object create() {
		return value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object object) {
		if (this == object)
			return true;
		if (object == null)
			return false;
		if (getClass() != object.getClass())
			return false;
		BasicTypeValue other = (BasicTypeValue) object;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

}
